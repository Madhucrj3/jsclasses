class Tracks{
    id;
    track=[];
    courseList=[];
    constructor(track){
        if(track!=undefined){
        this.track=track;
        this.id=track.id;
        track.course.map((course)=>{
            this.courseList.push(new Course(course));
        })
        }
    }
}
class Course{
    id;
    course={};
    topicList=[];
    constructor(course){
        if(course!=undefined){
        this.course=course;
        this.id=course.id;
        course.topic.map((topic)=>{
            this.topicList.push(new Topic(topic));
        })
    }
}
}
class Topic{
    id;
    topic;
    setslist=[];
    constructor(topic){
        if(topic!=undefined){
        this.id=topic.id;
        this.topic=topic;
        topic.sets.map((set)=>{
            if(set.name==="mcqSets")
            this.setslist.push(new McqSets(set));
            else
            if(set.name==="CodingSets")
            this.setslist.push(new CodingSets(set));
            else
            if(set.name==="AssignmentsSets")
            this.setslist.push(new AssignmentsSets(set));
            else
            if(set.name==="LearningSets")
            this.setslist.push(new LearningSets(set));
            else
            if(set.name==="PracticeSets")
            this.setslist.push(new PracticeSets(set));
        })
        }
    }
}
